# 在F12中找到banner.p，点进去看到很多行的文本
# 把两条线索收集完就很明显了，用pickle包读出banner.p中的东西

import pickle
with open('./src/banner.p','rb')  as f:
    data = pickle.load(f)
print(data)
print(type(data))
# 发现了一堆键值对，第一个估计是字符，第二个不像是ASCII，那么应该是数量
# 尝试打印出来估计就知道了

for p in data:
    for q in p:
        for i in range(q[1]):
            print(q[0],end="")
    print("")

